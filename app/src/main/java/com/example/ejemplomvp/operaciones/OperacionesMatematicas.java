package com.example.ejemplomvp.operaciones;

public class OperacionesMatematicas {

    public static float sumarDosNumeros (float numero1, float numero2) {
        float rta = numero1 + numero2;
        return rta;
    }
    public static float restarDosNumeros (float numero1, float numero2) {
        float rta = numero1 - numero2;
        return rta;
    }
    public static float multiplicarDosNumeros (float numero1, float numero2) {
        float rta = numero1 * numero2;
        return rta;
    }
    public static float dividirDosNumeros (float numero1, float numero2) {
        float rta = numero1 / numero2;
        return rta;
    }
    public static float calcularDosNumeros (float numero1, float numero2) {
        float rta = numero1 + numero2;
        return rta;
    }
}
